"""Builder Agent — Money Mode's automated tool-generation agent (V4).

The Builder Agent is triggered by Money Mode when a monetizable idea is
detected.  Its job is to:

1. Generate a Python tool/service scaffold via the engine LLM
2. Submit it to ForgeController for sandbox validation
3. Return the snapshot_id + validation result to Money Mode
4. The tool is deployed only after passing sandbox + ROI analysis

Flow::

    MoneyMode → BuilderAgent.build_tool(idea) →
        engine.generate(scaffold_prompt) →
        forge_controller.submit_change(auto_deploy=False) →
        Returns snapshot_id for manual / auto approval

Usage::

    from agents.builder_agent import get_builder_agent

    ba = get_builder_agent()
    result = ba.build_tool(
        name="email_validator_saas",
        description="SaaS micro-tool that validates email lists via REST API",
        tool_type="saas",
    )
    # result contains snapshot_id, validation, roi_analysis
"""
