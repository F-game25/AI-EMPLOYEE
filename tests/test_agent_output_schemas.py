class TestFinancialOutput:
    def test_valid_with_disclaimer(self):
        out = FinancialOutput(**_make_output(
            agent="turbo-quant",
            content=_FINANCIAL_CONTENT,
        ))
        assert out.agent == "turbo-quant"

    def test_missing_disclaimer_raises(self):
        with pytest.raises(ValidationError, match="disclaimer"):
            FinancialOutput(**_make_output(
                agent="turbo-quant",
                content="Just trade everything now.",
            ))

    def test_disclaimer_case_insensitive(self):
        out = FinancialOutput(**_make_output(
            agent="finance-wizard",
            content="Some analysis. Not constitute financial advice of any kind.",
        ))
        assert out.agent == "finance-wizard"


