# AGENT_SCHEMA_REGISTRY
# ═══════════════════════════════════════════════════════════════════════════════

class TestAgentSchemaRegistry:
    def test_registry_is_dict(self):
        assert isinstance(AGENT_SCHEMA_REGISTRY, dict)

    def test_all_values_are_agent_output_subclasses(self):
        for agent_id, cls in AGENT_SCHEMA_REGISTRY.items():
            assert issubclass(cls, AgentOutput), f"Schema for {agent_id!r} is not AgentOutput subclass"

    def test_key_agents_registered(self):
        key_agents = [
            "task-orchestrator", "lead-generator", "recruiter", "hr-manager",
            "finance-wizard", "turbo-quant", "arbitrage-bot",
            "brand-strategist", "web-researcher", "social-media-manager",
            "engineering-assistant", "growth-hacker", "ecom-agent",
            "project-manager", "company-builder",
        ]
        for agent_id in key_agents:
            assert agent_id in AGENT_SCHEMA_REGISTRY, f"{agent_id!r} missing from registry"

    def test_financial_agents_use_financial_output(self):
        financial = ["turbo-quant", "arbitrage-bot", "polymarket-trader", "signal-community", "finance-wizard"]
        for agent_id in financial:
            assert AGENT_SCHEMA_REGISTRY.get(agent_id) is FinancialOutput, \
                f"{agent_id!r} should use FinancialOutput"

    def test_hr_agents_use_hr_output(self):
        assert AGENT_SCHEMA_REGISTRY["recruiter"] is HROutput
        assert AGENT_SCHEMA_REGISTRY["hr-manager"] is HROutput


