# ═══════════════════════════════════════════════════════════════════════════════

class TestOutputValidationMiddleware:
    def _mw(self) -> OutputValidationMiddleware:
        return OutputValidationMiddleware()

    # ── validate() ────────────────────────────────────────────────────────────

    def test_validate_success(self):
        mw = self._mw()
        out = mw.validate("recruiter", "Candidate A is best.", ts="2026-01-01T00:00:00Z")
        assert isinstance(out, HROutput)
        assert out.agent == "recruiter"

    def test_validate_raises_on_empty_content(self):
        mw = self._mw()
        with pytest.raises(ValidationError):
            mw.validate("recruiter", "")

    def test_validate_raises_on_invalid_agent_id(self):
        mw = self._mw()
        with pytest.raises(ValidationError):
            mw.validate("bad agent!!", "some content")

    def test_validate_raises_on_financial_without_disclaimer(self):
        mw = self._mw()
        with pytest.raises(ValidationError):
            mw.validate("turbo-quant", "Buy everything now.", ts="2026-01-01T00:00:00Z")

    def test_validate_financial_with_disclaimer(self):
        mw = self._mw()
        out = mw.validate("turbo-quant", _FINANCIAL_CONTENT, ts="2026-01-01T00:00:00Z")
        assert isinstance(out, FinancialOutput)

    def test_validate_injects_ts_when_empty(self):
        mw = self._mw()
        out = mw.validate("recruiter", "Candidate response")
        assert out.ts != ""

    # ── validate_or_fallback() ────────────────────────────────────────────────

    def test_validate_or_fallback_success_returns_output(self):
        mw = self._mw()
        result, fallback = mw.validate_or_fallback("recruiter", "Candidate A is best.")
        assert result is not None
        assert fallback == ""

    def test_validate_or_fallback_failure_returns_fallback(self):
        mw = self._mw()
        result, fallback = mw.validate_or_fallback("recruiter", "")
        assert result is None
        assert fallback == FALLBACK_RESPONSE

    def test_validate_or_fallback_unknown_agent_uses_generic_schema(self):
        mw = self._mw()
        result, fallback = mw.validate_or_fallback("my-custom-agent", "Some output")
        assert result is not None
        assert isinstance(result, GenericAgentOutput)
        assert fallback == ""

    def test_validate_or_fallback_financial_no_disclaimer(self):
        mw = self._mw()
        result, fallback = mw.validate_or_fallback("turbo-quant", "Buy now!")
        assert result is None
        assert fallback == FALLBACK_RESPONSE

    def test_validate_or_fallback_financial_with_disclaimer(self):
        mw = self._mw()
        result, fallback = mw.validate_or_fallback("turbo-quant", _FINANCIAL_CONTENT)
        assert result is not None
        assert fallback == ""

    # ── Singleton ─────────────────────────────────────────────────────────────

    def test_singleton_same_instance(self):
        a = get_schema_validator()
        b = get_schema_validator()
        assert a is b

    # ── Fallback constant ─────────────────────────────────────────────────────

    def test_fallback_response_is_nonempty_string(self):
        assert isinstance(FALLBACK_RESPONSE, str)
        assert len(FALLBACK_RESPONSE) > 10


