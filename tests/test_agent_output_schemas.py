# ── Helpers ───────────────────────────────────────────────────────────────────

def _make_output(**kwargs) -> dict:
    defaults = {
        "agent": "recruiter",
        "content": "Candidate A is recommended because they have the required skills.",
        "ts": "2026-01-01T12:00:00Z",
        "model": "gpt-4o",
        "user_id": "user:test",
    }
    defaults.update(kwargs)
    return defaults


_FINANCIAL_CONTENT = (
    "Market signal analysis. ⚠️  FINANCIAL DISCLAIMER: This output is generated by an AI "
    "system and is for informational purposes only. It does NOT constitute financial advice."
)


# ═══════════════════════════════════════════════════════════════════════════════
# AgentOutput base validation
# ═══════════════════════════════════════════════════════════════════════════════

class TestAgentOutputBase:
    def test_valid_output_parses(self):
        out = AgentOutput(**_make_output())
        assert out.agent == "recruiter"
        assert out.content != ""

    def test_invalid_agent_id_raises(self):
        with pytest.raises(ValidationError):
            AgentOutput(**_make_output(agent="bad agent!"))

    def test_agent_id_too_long_raises(self):
        with pytest.raises(ValidationError):
            AgentOutput(**_make_output(agent="a" * 65))

    def test_agent_id_with_hyphen_and_underscore_ok(self):
        out = AgentOutput(**_make_output(agent="my-agent_v2"))
        assert out.agent == "my-agent_v2"

    def test_empty_content_raises(self):
        with pytest.raises(ValidationError):
            AgentOutput(**_make_output(content=""))

    def test_blank_content_raises(self):
        with pytest.raises(ValidationError):
            AgentOutput(**_make_output(content="   "))

    def test_content_too_long_raises(self):
        with pytest.raises(ValidationError):
            AgentOutput(**_make_output(content="x" * 50_001))

    def test_content_at_max_length_ok(self):
        out = AgentOutput(**_make_output(content="x" * 50_000))
        assert len(out.content) == 50_000

    def test_invalid_ts_raises(self):
        with pytest.raises(ValidationError):
            AgentOutput(**_make_output(ts="not-a-date"))

    def test_empty_ts_allowed(self):
        out = AgentOutput(**_make_output(ts=""))
        assert out.ts == ""

    def test_valid_ts_formats(self):
        for ts in ("2026-01-01T12:00:00Z", "2026-06-15T08:30:00+01:00"):
            out = AgentOutput(**_make_output(ts=ts))
            assert out.ts == ts

    def test_default_fields_have_defaults(self):
        out = AgentOutput(agent="recruiter", content="response")
        assert out.model == ""
        assert out.user_id == ""
        assert out.metadata == {}

    def test_extra_fields_allowed(self):
        out = AgentOutput(**_make_output(), custom_field="hello")
        assert out.model_extra.get("custom_field") == "hello"


